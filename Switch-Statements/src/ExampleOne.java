public class ExampleOne {
    public static void main(String[] args) {
        int value = 9;

        switch(value) {
            case 2:
                System.out.println("Lower");
                break;
            case 5:
                System.out.println("Equal");
                break;
            case 8:
                System.out.println("Greater");
                break;
            default:
                System.out.println("Default");
                break;

        }

    }
}
