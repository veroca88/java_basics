package com.company;

public class Main {

    public static void main(String[] args) {
	// Concatenate numbers
        int x = 2;
        int y = 3;
        System.out.println("The sum of x and y is " + x + y);
        // output The sum of x and y is 23

        System.out.println("The sum of x and y is " + (x + y));
        // output The sum of x and y is 5

    }
}
